function solution(s) {
    var answer = '';
    var arr = [];

    for (var i = 0; i < s.length; i++) {
        arr.push(s[i]);
    }

    arr.sort(sortReverse);

    for (var i = 0; i < arr.length; i++) {
        answer += arr[i];
    }

    return answer;
}

function sortReverse(a, b) {
    if (a > b) return -1;
    else if (b > a) return 1;
    else return 0;
}

