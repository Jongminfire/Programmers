function solution(s) {
    var answer = true;
    var count_p = 0;
    var count_y = 0;

    for (var i = 0; i < s.length; i++) {
        if (s[i] === 'p' || s[i] === 'P')
            count_p++;

        else if (s[i] === 'y' || s[i] === 'Y')
            count_y++;
    }

    if (count_p === count_y)
        answer = true;
    else
        answer = false;

    return answer;
}

